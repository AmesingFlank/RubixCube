public class Point3DFloat {
    public float x;
    public float y;
    public float z;
    public Point3DFloat(float xin,float yin,float zin){
        x=xin;
        y=yin;
        z=zin;
    }
    public float getX(){
        return x;
    }
    public float getY(){
        return y;
    }
    public float getZ(){
        return z;
    }
}
